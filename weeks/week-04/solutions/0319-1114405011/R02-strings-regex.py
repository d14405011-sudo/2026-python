# =====================================
# R02. 正則表達式：搜尋、替換、旗標（2.4–2.8）
# =====================================
# 本模組演示正規表達式的核心操作：
# 1. re.compile() - 編譯正規表達式模式以供重複使用
# 2. findall() / match() / finditer() - 搜尋文本中的符合項
# 3. sub() / subn() - 進行字串替換
# 4. 旗標（flags）- 控制匹配行為（如忽略大小寫、多行模式）
# 5. 貪婪 vs 非貪婪匹配 - 控制匹配範圍
# 6. DOTALL - 讓 . 匹配包括換行符

import re


# ── 2.4 匹配和搜尋 ────────────────────────────────────────
# 目標：從文本中找出所有符合特定模式的項目
# 例如：找出日期（MM/DD/YYYY 格式）

text = "Today is 11/27/2012. PyCon starts 3/13/2013."

# re.compile() - 預先編譯正規表達式模式以提高效率
# r"(\d+)/(\d+)/(\d+)" - 正規表達式說明：
#   \d+ 表示「一個或多個數字」
#   () 表示「捕獲群組」（括起來的部分會被提取出來）
#   共有 3 個捕獲群組：月、日、年
datepat = re.compile(r"(\d+)/(\d+)/(\d+)")

# findall() - 找出所有符合的子字串，返回所有捕獲群組的列表
print(datepat.findall(text))
# 輸出: [('11', '27', '2012'), ('3', '13', '2013')]
#      每個日期都被解析成 (月, 日, 年) 的元組

# match() - 從字串的開頭匹配（只匹配第一次出現）
m = datepat.match("11/27/2012")
assert m is not None  # 確保匹配成功

# m.group(0) - 返回整個符合的字串
# m.groups() - 返回所有捕獲群組組成的元組
print(m.group(0), m.groups())  # 輸出: 11/27/2012 ('11', '27', '2012')

# finditer() - 返回迭代器，逐個返回每個符合結果
# 這樣可以逐次處理已找到的匹配項
for m in datepat.finditer(text):
    # 從捕獲群組中提取月、日、年
    month, day, year = m.groups()
    # 將日期格式改為 YYYY-MM-DD（國際標準格式）
    print(f"{year}-{month}-{day}")


# ── 2.5 搜尋和替換 ────────────────────────────────────────
# 目標：用新的格式替換文本中符合模式的部分

# re.sub() - 替換文本中符合模式的所有部分
# r"(\d+)/(\d+)/(\d+)" - 搜尋模式（含 3 個捕獲群組）
# r"\3-\1-\2" - 替換模式
#   \1、\2、\3 代表第 1、2、3 個捕獲群組
#   這裡把 (月/日/年) 改成 (年-月-日)
print(re.sub(r"(\d+)/(\d+)/(\d+)", r"\3-\1-\2", text))
# 輸出: 'Today is 2012-11-27. PyCon starts 2013-3-13.'

# 更可讀的方式：使用命名捕獲群組
# (?P<name>pattern) - 給捕獲群組取一個名字
# \g<name> - 在替換中用名字引用捕獲群組
print(
    re.sub(
        r"(?P<month>\d+)/(?P<day>\d+)/(?P<year>\d+)",
        r"\g<year>-\g<month>-\g<day>",
        text,
    )
)
# 輸出: 同上，但使用命名群組讓程式碼更清晰易懂

# re.subn() - 和 re.sub() 類似，但同時返回替換後的字串和替換次數
newtext, n = re.subn(r"(\d+)/(\d+)/(\d+)", r"\3-\1-\2", text)
print(f"替換了 {n} 次")  # 輸出: 替換了 2 次（因為有 2 個日期）


# ── 2.6 忽略大小寫 ────────────────────────────────────────
# 目標：不區分大小寫地進行字串匹配

s = "UPPER PYTHON, lower python, Mixed Python"

# re.findall() 搜尋所有符合項
# flags=re.IGNORECASE - 旗標：忽略大小寫（同義詞為 re.I）
# 即使搜尋 "python"（小寫），也會找到 PYTHON、python、Python
print(re.findall("python", s, flags=re.IGNORECASE))
# 輸出: ['PYTHON', 'python', 'Python']
#      找到了全部 3 個符合項，無論其大小寫


# ── 2.7 非貪婪（最短匹配）───────────────────────────────────
# 目標：理解貪婪量詞 (*、+) 和非貪婪量詞 (*?、+?) 的差異
# 貪婪：盡可能匹配最長的字串
# 非貪婪：盡可能匹配最短的字串

text2 = 'Computer says "no." Phone says "yes."'

# 貪婪匹配：.* 會匹配盡量多的字元
# 結果：從第一個 " 一直匹配到最後一個 "
print(re.compile(r'"(.*)"').findall(text2))  
# 輸出: ['no." Phone says "yes.']
#      這不是我們想要的—它跨越了兩個引號之間的所有內容

# 非貪婪匹配：.*? 會匹配儘可能少的字元
# 結果：每次都在最近的 " 處停止，正確地提取出引號內的文字
print(re.compile(r'"(.*?)"').findall(text2))  
# 輸出: ['no.', 'yes.']
#      正確地分別提取了兩個引號包圍的字串


# ── 2.8 多行匹配（DOTALL）───────────────────────────────────
# 目標：讓正規表達式的 . 位置符匹配包括換行符在內的任何字元
# 注意：預設情況下 . 只匹配除了換行符之外的任何字元

code = "/* this is a\nmultiline comment */"

# re.DOTALL 旗標讓 . 也能匹配 \n（換行符）
# 沒有這個旗標，.* 會在第一個 \n 處停止；
# 有這個旗標，.* 就能匹配整個多行註解
print(re.compile(r"/\*(.*?)\*/", re.DOTALL).findall(code))
# 輸出: [' this is a\nmultiline comment ']
#      成功提取了包含換行符的完整註解內容
