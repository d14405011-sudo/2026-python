# U1. 解包失敗的原因：變數數量 ≠ 元素數量（1.1）
# 
# 解包（Unpacking）是 Python 中將可迭代物件的元素指派給多個變數的操作。
# 解包成功的前提：變數數量必須 == 可迭代物件的元素數量
# 若數量不匹配，Python 會拋出 ValueError

# 建立一個包含 2 個元素的 tuple
p = (4, 5)

# 正確的解包：變數數量 (2) == 元素數量 (2)
# x 會被指派 4，y 會被指派 5
x, y = p

# 輸出 tuple 本身（會顯示 (4, 5)）
print(p)

# 輸出已解包的變數，格式為 4,5
print(f'{x},{y}')