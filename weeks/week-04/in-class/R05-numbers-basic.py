# 2. Decimal - 精確的十進制浮點數運算
# 3. format() - 靈活的數字格式化
# 4. bin() / oct() / hex() - 進制轉換

from decimal import Decimal, localcontext  # 用於精確浮點數計算
import math  # 導入 math 模組用於高精度運算


# ── 3.1 四捨五入 ───────────────────────────────────────────
# 目標：將浮點數四捨五入到指定的小數位數或到整數

# round(number, ndigits) 的用法
# 第一個參數：要四捨五入的數字
# 第二個參數：要保留的小數位數（正數表示小數位，負數表示整數位）

# 基本四捨五入：保留 1 位小數
print(round(1.27, 1))  
# 輸出: 1.3
#      1.27 四捨五入到 1 位小數得到 1.3

# 保留 3 位小數
print(round(1.25361, 3))  
# 輸出: 1.254
#      1.25361 四捨五入到 3 位小數得到 1.254

# 整數四捨五入（0.5 的特殊情況）
# Python 使用「銀行家捨入」（Banker's rounding）：
# 當小數部分恰好是 0.5 時，四捨五入到最近的偶數
print(round(0.5))  
# 輸出: 0
#      0.5 四捨五入到偶數 0（不是 1）

print(round(2.5))  
# 輸出: 2
#      2.5 四捨五入到偶數 2（不是 3）

# 對整數位進行四捨五入（使用負數）
a = 1627731
# round(a, -2) 表示四捨五入到百位（10^2 = 100）
print(round(a, -2))  
# 輸出: 1627700
#      1627731 四捨五入到百位得到 1627700


# ── 3.2 精確浮點數 ────────────────────────────────────────────
# 目標：處理浮點數運算中的精度誤差問題

# 浮點數的精度問題
# 計算機用二進制表示浮點數，某些十進制小數無法精確表示
print(4.2 + 2.1)  
# 輸出: 6.300000000000001
#      本應等於 6.3，但因為二進制表示的誤差，出現了額外的 1

# 使用 Decimal 型別進行精確計算
# Decimal 是 Python 的高精度十進制數字型別
da, db = Decimal("4.2"), Decimal("2.1")
print(da + db)  
# 輸出: 6.3
#      使用 Decimal 得到正確的結果

# 使用 localcontext() 設定精度上下文
# ctx.prec = 3 表示設定精度為 3 位有效數字
with localcontext() as ctx:
    ctx.prec = 3  # 設定運算精度為 3 位有效數字
    print(Decimal("1.3") / Decimal("1.7"))  
    # 輸出: 0.765
    #      進行除法運算，有效數字限制為 3 位

# math.fsum() 函式用於高精度的序列求和
# 它特別適合於包含極大和極小數字混合的情況
# 例如：1.23e18（非常大的數）+ 1（很小的數）+ -1.23e18
print(math.fsum([1.23e18, 1, -1.23e18]))  
# 輸出: 1.0
#      正確的結果（如果用普通 sum() 會丟失 1）
#      這是因為 1.23e18 和 -1.23e18 會相互抵消，但相對來說 1 太小


# ── 3.3 數字格式化 ────────────────────────────────────────────
# 目標：將數字轉換為特定格式的字串（用於顯示、輸出）

x = 1234.56789

# format(value, format_spec) 的各種用法
# format_spec 的通用格式為：
# [[fill]align][sign][#][0][width][grouping_option][.precision][type]

# "0.2f" - 固定小數點格式，保留 2 位小數（四捨五入）
print(format(x, "0.2f"))  
# 輸出: '1234.57'
#      x = 1234.56789 四捨五入到 2 位小數

# ">10.1f" - 右對齐，寬度 10，保留 1 位小數
print(format(x, ">10.1f"))  
# 輸出: '    1234.6'
#      在寬度 10 內右對齐，左邊用空格填充

# "," - 每三位用逗號分隔（千位分隔符）
print(format(x, ","))  
# 輸出: '1,234.56789'
#      保留原有的小數位數，但加上千位分隔符

# "0,.2f" - 千位分隔符 + 固定 2 位小數
print(format(x, "0,.2f"))  
# 輸出: '1,234.57'
#      既有千位分隔符，又有 2 位小數

# "e" - 科學記號格式（預設 6 位小數）
print(format(x, "e"))  
# 輸出: '1.234568e+03'
#      1234.56789 以科學記號表示（1.234568 × 10³）


# ── 3.4 二八十六進制 ─────────────────────────────────────────
# 目標：在十進制、二進制、八進制、十六進制之間轉換

n = 1234

# bin() - 轉換為二進制（Binary）字串
# oct() - 轉換為八進制（Octal）字串
# hex() - 轉換為十六進制（Hexadecimal）字串
# 輸出都會帶上前綴：0b（二進制）、0o（八進制）、0x（十六進制）
print(bin(n), oct(n), hex(n))  
# 輸出: 0b10011010010 0o2322 0x4d2
#      1234 的三種進制表示

# 使用 format() 進行進制轉換（不帶前綴）
# "b" - 二進制
# "o" - 八進制
# "x" - 十六進制（小寫）
# "X" - 十六進制（大寫）
print(format(n, "b"), format(n, "x"))  
# 輸出: 10011010010 4d2
#      同上，但沒有進制前綴

# int(string, base) - 將字串轉換回十進制
# 第一個參數：要轉換的字串
# 第二個參數：該字串的進制數（2~36）

# 從十六進制字串 "4d2" 轉換回十進制
print(int("4d2", 16))  
# 輸出: 1234
#      0x4d2 = 4×16² + 13×16¹ + 2×16⁰ = 1024 + 208 + 2 = 1234

# 從八進制字串 "2322" 轉換回十進制
print(int("2322", 8))  
# 輸出: 1234
#      0o2322 = 2×8³ + 3×8² + 2×8¹ + 2×8⁰ = 1024 + 192 + 16 + 2 = 1234
