"""
UVA 272 / ZeroJudge c007 - TeX Quotes（TeX 引號轉換）
======================================================
【題目概要】
  將輸入文字中的所有普通雙引號 " 依序替換為 TeX 方向性引號：
    第 1 個 " → ``  （開引號，兩個左單引號）
    第 2 個 " → ''  （閉引號，兩個右單引號）
    第 3 個 " → ``  （再次開引號）
    第 4 個 " → ''  ...依此類推（奇數 → 開，偶數 → 閉）
  其他所有字元（包含換行）維持不變。

【核心做法】
  - 用一個布林旗標 `open_quote` 記錄「下一個 " 應替換為開引號還是閉引號」。
  - 逐字元掃描，遇到 " 就依旗標輸出 `` 或 ''，然後翻轉旗標。
  - 非 " 字元直接輸出，不做任何處理。

【輸入格式】
  若干行文字，以 EOF 結束，保證雙引號數量為偶數。

【輸出格式】
  與輸入完全相同，僅把 " 換成對應的 TeX 引號。
"""

import sys


# ─────────────────────────────────────────────────────────────────────────────
def convert(input_text: str) -> str:
    """
    將文字中的所有普通雙引號 " 依序轉換為 TeX 方向性引號。

    【轉換規則】
      - 以 open_quote 旗標追蹤「目前應輸出開引號還是閉引號」
      - open_quote = True  → 遇到 " 輸出 ``，然後設為 False
      - open_quote = False → 遇到 " 輸出 ''，然後設為 True
      - 非 " 字元原樣保留

    【為什麼逐字元處理？】
      因為一行中可能同時出現多個 " ，且需要跨行記憶旗標狀態，
      所以一次處理整段文字而非一行行獨立處理。

    參數：
        input_text (str): 原始輸入文字（可含多行與換行符）

    回傳：
        str: 替換後的文字（結構與輸入相同）
    """
    result      = []        # 收集轉換後的字元，最後一次性串接（效率優於字串相加）
    open_quote  = True      # True = 下一個 " 應換成 ``（開引號）

    for ch in input_text:
        if ch == '"':
            if open_quote:
                result.append('``')     # 開引號：兩個左單引號
            else:
                result.append("''")     # 閉引號：兩個右單引號
            open_quote = not open_quote # 翻轉旗標，下次換另一種引號
        else:
            result.append(ch)           # 非引號字元：原樣保留

    return ''.join(result)  # 將列表串接為字串（比逐步 += 快）


# ── 直接執行時：從 stdin 讀入，輸出至 stdout ─────────────────────────────────
if __name__ == "__main__":
    # 一次讀取全部輸入，轉換後印出
    # end='' 避免多加一個換行（輸入結尾若無換行，輸出也不應有）
    print(convert(sys.stdin.read()), end='')
