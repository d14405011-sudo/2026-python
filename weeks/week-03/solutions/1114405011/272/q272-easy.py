"""
UVA 272 / ZeroJudge c007 - TeX Quotes 簡易版
=============================================
【與 q272.py 的差異】
  q272.py     → 逐字元掃描，用旗標 + 列表收集
  q272-easy.py → 用 str.replace() 搭配計數器，一次替換一個 "，
                 非常接近「手動逐步取代」的直覺思路

【核心簡化想法】
  Python 的 str.replace(old, new, count=1) 可以只替換「第 1 個」符合的字串。
  每次呼叫就把最前面的 " 換掉，然後換另一種引號，再找下一個 "，
  直到文字裡沒有 " 為止。

  用一個清單 cycle = ['``', "''"] 輪流取值，
  搭配 i % 2 選擇當前應使用哪種引號。
"""

import sys


# ─────────────────────────────────────────────────────────────────────────────
def convert(input_text: str) -> str:
    """
    用「逐一取代最前面的雙引號」方法，把 " 換成 TeX 方向性引號。

    【做法說明】
      ① 建立一個輪替序列 QUOTES = ['``', "''" ]
         索引 0 → 開引號 ``
         索引 1 → 閉引號 ''
      ② 用計數器 i 記錄「已替換了幾個 "」
      ③ 每次用 replace('"', QUOTES[i % 2], 1) 只換最前面那個 "
      ④ 計數器加 1，繼續找下一個 "，直到文字中沒有 " 為止

    【為什麼簡單？】
      不需要手動逐字元掃描，Python 的 replace() 幫你找位置；
      只需理解「奇數個 → 開引號，偶數個 → 閉引號」這個規律。

    參數：
        input_text (str): 原始輸入文字

    回傳：
        str: 替換後的文字
    """
    # 兩種 TeX 引號，依索引輪替：0 = 開引號，1 = 閉引號
    QUOTES = ['``', "''"]

    text = input_text   # 複製一份，逐步修改
    i    = 0            # 已替換的引號數量（從 0 開始）

    # 只要文字中還有 " 就繼續替換
    while '"' in text:
        text = text.replace('"', QUOTES[i % 2], 1)  # 只換第 1 個 "
        i += 1  # 換完一個，計數加 1，下次輪另一種引號

    return text


# ── 直接執行時：從 stdin 讀入，輸出至 stdout ─────────────────────────────────
if __name__ == "__main__":
    print(convert(sys.stdin.read()), end='')
