"""
UVA 118 / ZeroJudge c082 - Martian Robots（火星機器人）簡易版
==============================================================
【與 q118.py 的差異】
  q118.py     → 用索引運算（加減 % 4）實作轉向，需要理解列表索引
  q118-easy.py → 用兩個字典直接記錄「下一個方向」，看了馬上懂

【核心簡化想法】
  轉向不用算索引，直接查表：
    右轉字典 → {'N':'E', 'E':'S', 'S':'W', 'W':'N'}
    左轉字典 → {'N':'W', 'W':'S', 'S':'E', 'E':'N'}
  移動也用字典查位移，不用 tuple 解包。

  整個模擬邏輯寫在 solve() 裡，不拆成多個函式，一次看完。
"""

import sys

# ── 轉向查表（右轉：順時針；左轉：逆時針）────────────────────────────────────
# 用字典直接寫出「方向 → 轉完後的方向」，背起來比索引運算直覺多了
RIGHT = {'N': 'E', 'E': 'S', 'S': 'W', 'W': 'N'}  # 右轉一格
LEFT  = {'N': 'W', 'W': 'S', 'S': 'E', 'E': 'N'}  # 左轉一格

# ── 各方向的 x、y 位移 ────────────────────────────────────────────────────────
# 北：y+1　東：x+1　南：y-1　西：x-1
DX = {'N':  0, 'E':  1, 'S':  0, 'W': -1}
DY = {'N':  1, 'E':  0, 'S': -1, 'W':  0}


# ─────────────────────────────────────────────────────────────────────────────
def solve(input_text: str) -> str:
    """
    讀入完整輸入，輸出每個機器人的最終狀態。

    【輸入格式】
      第一行：max_x max_y（網格右上角座標）
      之後每兩行一組：
        行 1：x y 方向
        行 2：指令字串（L / R / F）

    【輸出格式】
      存活：x y 方向
      掉落：x y 方向 LOST

    【程式流程】
      ① 讀入網格大小與機器人資料
      ② 對每個機器人逐字元執行指令：
           L → 查 LEFT 字典，取得左轉方向
           R → 查 RIGHT 字典，取得右轉方向
           F → 計算下一格座標：
               • 在邊界內 → 正常移動
               • 超出邊界 + 有 scent → 忽略此 F（保持不動）
               • 超出邊界 + 無 scent → 留下 scent、標記 LOST、停止
      ③ 依存活 / 掉落輸出結果

    參數：
        input_text (str): 完整輸入文字

    回傳：
        str: 完整輸出文字
    """
    # 過濾空白行，避免解析出錯
    lines = [l.strip() for l in input_text.strip().splitlines() if l.strip()]

    # 第一行：讀取網格右上角座標
    max_x, max_y = map(int, lines[0].split())

    scents = set()  # 儲存「曾有機器人在此掉落」的座標，供後續機器人參考
    out    = []     # 收集每個機器人的輸出行

    i = 1           # 從第二行開始，每次讀兩行（位置行 + 指令行）

    while i + 1 < len(lines):

        # ── 讀取機器人初始狀態 ────────────────────────────────────────────────
        parts = lines[i].split()
        x, y, d = int(parts[0]), int(parts[1]), parts[2]
        cmds = lines[i + 1]   # 指令字串，例如 "RFRFRFRF"
        i += 2                # 移到下一組機器人

        lost = False          # 預設為存活

        # ── 逐字元執行指令 ────────────────────────────────────────────────────
        for cmd in cmds:

            if cmd == 'L':
                # 左轉：查 LEFT 字典，直接得到新方向
                d = LEFT[d]

            elif cmd == 'R':
                # 右轉：查 RIGHT 字典，直接得到新方向
                d = RIGHT[d]

            elif cmd == 'F':
                # 計算前進後的座標
                nx = x + DX[d]
                ny = y + DY[d]

                if 0 <= nx <= max_x and 0 <= ny <= max_y:
                    # 在邊界內：正常移動
                    x, y = nx, ny

                elif (x, y) in scents:
                    # 超出邊界 + 有 scent：忽略這個 F，維持原位
                    pass  # 什麼都不做，繼續下一個指令

                else:
                    # 超出邊界 + 無 scent：機器人掉落
                    scents.add((x, y))  # 在掉落前的座標留下 scent
                    lost = True
                    break               # 後續指令不再執行

        # ── 組合輸出行 ────────────────────────────────────────────────────────
        if lost:
            out.append(f"{x} {y} {d} LOST")   # 掉落：加上 LOST
        else:
            out.append(f"{x} {y} {d}")         # 存活：只輸出座標與方向

    return "\n".join(out)   # 用換行串接所有結果，結尾不多加換行


# ── 直接執行時：從鍵盤（stdin）讀入，輸出至螢幕（stdout）────────────────────
if __name__ == "__main__":
    print(solve(sys.stdin.read()))
